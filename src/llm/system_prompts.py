def get_system_prompt_template(lang: str) -> str:
    normalized_lang = lang.strip().lower()

    if normalized_lang in SYSTEM_PROMPTS_BY_LANG:
        return SYSTEM_PROMPTS_BY_LANG[normalized_lang]

    raise RuntimeError(
        f"Для языка '{lang}' пока не задан system prompt. "
    )